    private void trocaQtde(String operador) {
        int qtde = Integer.parseInt(txtQtde.getText());
        switch (operador) {
            case "ADD":
                qtde++;
                txtQtde.setText(String.valueOf(qtde));
                break;
            case "SUB":
                qtde--;
                txtQtde.setText(String.valueOf(qtde));
            default:
                txtQtde.setText(String.valueOf(qtde));
        }
    }

