    private void carregarDados() {
        contaReceberTableModel = new ContaReceberTableModel(
                contaReceberController.findContasReceberHoje()
        );
        tblDados.setModel(contaReceberTableModel);
        contaReceberTableModel.fireTableDataChanged();
    }

    private void buscaContaReceber() {
        contaReceberTableModel = new ContaReceberTableModel(
                contaReceberController.findContasReceberByCliente(txtFiltro.getText())
        );
        tblDados.setModel(contaReceberTableModel);
        contaReceberTableModel.fireTableDataChanged();
    }
