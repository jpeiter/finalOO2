    private void btnDetalhesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetalhesActionPerformed
        try {
            if (tblDados.getSelectedRow() >= 0) {
                Long idContaReceber = Long.parseLong(
                        contaReceberTableModel.getValueAt(
                                tblDados.getSelectedRow(), 0).toString()
                );
                FrmContaReceberForm frm
                        = new FrmContaReceberForm(null, true);
                frm.carregarContaReceber(idContaReceber);
                frm.setLocationRelativeTo(null);
                frm.setVisible(true);

                carregarDados();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Selecione um registro!",
                        "Atenção",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Ocorreu um erro!",
                    "Atenção",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDetalhesActionPerformed

