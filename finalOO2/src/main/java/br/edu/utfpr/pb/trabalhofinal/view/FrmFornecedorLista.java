    private void carregarDados() {
        fornecedorTableModel = new FornecedorTableModel(
                fornecedorController.listar()
        );
        tblDados.setModel(fornecedorTableModel);
        fornecedorTableModel.fireTableDataChanged();
    }

    private void buscaFornecedor() {
        if (txtPesquisar.getText().isEmpty()) {
            fornecedorTableModel = new FornecedorTableModel(
                    fornecedorController.listar()
            );
        } else {
            if (rdbNome.isSelected()) {
                fornecedorTableModel = new FornecedorTableModel(
                        fornecedorController.findFornecedorByNome(txtPesquisar.getText())
                );
            } else {
                fornecedorTableModel = new FornecedorTableModel(
                        fornecedorController.findFornecedorByCNPJ(txtPesquisar.getText().replaceAll("[/.-]", "")));
            }
        }
        tblDados.setModel(fornecedorTableModel);
        fornecedorTableModel.fireTableDataChanged();
    }
